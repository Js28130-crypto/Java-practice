package Ushtrime1;

public class Ushtrimi5 {
    public static void main(String[] args) {
        int sum = 0;    //instance variable
        //for-loop(initialization, condition, increment)
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
            sum += i;
        }
        System.out.println(sum);
    }
}
